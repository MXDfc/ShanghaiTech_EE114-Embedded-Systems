#ifndef _KEY_H
#define _KEY_H

/*
 * key.h — 按键事件模块接口
 *
 * 设计说明：
 *   本模块仅负责检测按键的物理状态并将其抽象为"事件"，
 *   不关心按键事件的后续处理逻辑（由调用方决定）。
 *   通过枚举值区分短按与长按，实现与上层逻辑的解耦。
 */

#include "main.h"
#include <stdint.h>

/* -----------------------------------------------------------------------
 * 按键事件枚举
 *   KEY_EVENT_NONE  : 无事件（按键稳定或抖动中）
 *   KEY_EVENT_SHORT : 短按事件，按下时间 < 长按阈值，松手后触发一次
 *   KEY_EVENT_LONG  : 长按事件，按下时间 ≥ 长按阈值，到达阈值时立即触发一次
 *                     （无需等待松手，触发后继续按住不再产生新事件）
 * ----------------------------------------------------------------------- */
typedef enum {
    KEY_EVENT_NONE  = 0,
    KEY_EVENT_SHORT = 1,
    KEY_EVENT_LONG  = 2,
} KeyEvent_t;

/* -----------------------------------------------------------------------
 * KEY_Init
 *   初始化按键模块。
 *   调用时机：在 MX_GPIO_Init() 之后调用（GPIO 由 CubeMX 已配置好）。
 * ----------------------------------------------------------------------- */
void KEY_Init(void);

/* -----------------------------------------------------------------------
 * KEY_Scan
 *   按键扫描函数，每次调用检测一次按键状态并更新内部状态机。
 *
 *   调用要求：
 *     - 必须在主循环中以固定周期（推荐 10 ms）定时调用
 *     - 同一次按键最多触发一个事件：长按触发后不再补发短按事件
 *
 *   返回值：
 *     - KEY_EVENT_SHORT : 检测到一次有效短按（已消抖）
 *     - KEY_EVENT_LONG  : 检测到长按（达到长按阈值时立即返回）
 *     - KEY_EVENT_NONE  : 无事件
 * ----------------------------------------------------------------------- */
KeyEvent_t KEY_Scan(void);

#endif /* _KEY_H */
