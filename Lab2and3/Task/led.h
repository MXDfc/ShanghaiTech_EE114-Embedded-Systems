#ifndef _LED_H
#define _LED_H

/*
 * led.h — LED 控制模块接口
 *
 * 设计说明：
 *   本模块将 LED 的驱动细节（GPIO 操作）与上层控制逻辑完全隔离。
 *   调用方（如 main.c 中的按键事件处理）只需调用语义清晰的接口函数，
 *   无需了解底层引脚配置。更换 LED 所连接的外设时，仅修改 led.c
 *   顶部的硬件适配宏即可，无需改动任何调用处的代码。
 *
 * 模块层次结构：
 *   [ 调用方（main.c / 其他模块）]
 *           ↓ LED_Enable / LED_Disable / LED_NextMode / LED_Process
 *   [ LED 控制层（led.c：模式状态机）]
 *           ↓ LED_SetPin（内部静态函数）
 *   [ 硬件适配层（led.c 顶部宏：GPIO 引脚定义）]
*/

#include "main.h"
#include <stdint.h>

/* -----------------------------------------------------------------------
 * LED 显示模式枚举
 *   LED_MODE_STEADY     : 常亮
 *   LED_MODE_SLOW_BLINK : 慢闪（约 1 Hz，500 ms 亮 / 500 ms 灭）
 *   LED_MODE_FAST_BLINK : 快闪（约 4 Hz，125 ms 亮 / 125 ms 灭）
 *   LED_MODE_BREATH     : 呼吸灯（软件 PWM 模拟亮度渐变，2 秒一个周期）
 *   LED_MODE_COUNT      : 模式总数，仅供 led.c 内部循环使用，勿在外部引用
 * ----------------------------------------------------------------------- */
typedef enum {
    LED_MODE_STEADY     = 0,
    LED_MODE_SLOW_BLINK = 1,
    LED_MODE_FAST_BLINK = 2,
    LED_MODE_BREATH     = 3,
    LED_MODE_COUNT      = 4,
} LedMode_t;

/* -----------------------------------------------------------------------
 * LED_Init
 *   初始化 LED 引脚为推挽输出，初始状态为关闭。
 *
 *   调用时机：
 *     必须在 MX_TIM1_Init() 之后调用，因为 CubeMX 会将 PB13 配置为
 *     TIM1_CH1N 复用推挽，LED_Init 会将其重配为普通推挽输出。
 * ----------------------------------------------------------------------- */
void LED_Init(void);

/* -----------------------------------------------------------------------
 * LED_Enable / LED_Disable
 *   控制 LED 整体开关（对应长按事件）。
 *
 *   LED_Enable  : 开启 LED，恢复上次记忆的显示模式，由 LED_Process 驱动输出。
 *   LED_Disable : 关闭 LED，立即熄灭，LED_Process 在关闭状态下不驱动输出。
 *
 *   注意：调用 LED_Enable 后，需持续调用 LED_Process 才能看到实际效果。
 * ----------------------------------------------------------------------- */
void LED_Enable(void);
void LED_Disable(void);

/* -----------------------------------------------------------------------
 * LED_TogglePower
 *   切换 LED 开关状态（开→关 或 关→开）。
 *   封装 LED_Enable/LED_Disable 的条件判断，简化调用方代码。
 *   典型用途：长按事件回调。
 * ----------------------------------------------------------------------- */
void LED_TogglePower(void);

/* -----------------------------------------------------------------------
 * LED_NextMode
 *   切换至下一个显示模式，顺序循环：
 *     常亮 → 慢闪 → 快闪 → 呼吸 → 常亮 → …
 *
 *   注意：仅在 LED 处于开启状态（LED_Enable 已调用）下有效；
 *         关闭状态下调用此函数不产生任何效果。
 *   典型用途：短按事件回调。
 * ----------------------------------------------------------------------- */
void LED_NextMode(void);

/* -----------------------------------------------------------------------
 * LED_Process
 *   LED 效果驱动函数，根据当前模式和系统时钟更新 LED 输出。
 *
 *   参数：
 *     tick — 当前系统毫秒计数，直接传入 HAL_GetTick() 的返回值即可
 *
 *   调用要求：
 *     - 必须在主循环中每次迭代均调用，切勿在主循环中使用 HAL_Delay
 *     - 常亮/慢闪/快闪模式：10 ms 粒度调用即可正常工作
 *     - 呼吸灯模式：需约 1 ms 粒度调用以获得平滑的亮度渐变效果；
 *       若调用粒度较大（如 10 ms），呼吸效果会退化为明显的跳变，但功能仍正确
 *     - 亮度调节生效依赖同样的调用粒度：粒度越小，软件 PWM 的亮度分辨率越高
 * ----------------------------------------------------------------------- */
void LED_Process(uint32_t tick);

/* -----------------------------------------------------------------------
 * LED_SetBrightness
 *   设置 LED 的全局亮度级别，对所有显示模式均有效。
 *
 *   参数：
 *     level — 亮度百分比，取值范围 0 ~ 100
 *               0   : 完全熄灭（等效于 LED_Disable，但不改变开关状态，
 *                     重新设置亮度 > 0 后 LED 恢复，无需重新调用 LED_Enable）
 *               100 : 全亮（默认值，不启用亮度 PWM，GPIO 始终输出高/低电平）
 *               1~99: 通过软件 PWM 模拟中间亮度
 *
 *   与各模式的交互说明：
 *     - 常亮模式    : 亮度直接决定稳态输出的 PWM 占空比
 *     - 慢闪/快闪   : 仅在"点亮"阶段内施加亮度 PWM，"熄灭"阶段不受影响
 *     - 呼吸灯模式  : 亮度作为缩放系数乘入呼吸曲线的 duty 值，
 *                     保持渐变效果的同时整体压低峰值亮度；
 *                     level=50 时呼吸灯峰值亮度约为全亮的 50%
 *
 *   注意：
 *     - 此函数仅更新内部状态，实际输出在下一次 LED_Process 调用时生效
 *     - LED 处于关闭状态（LED_Disable 已调用）时，设置亮度不产生视觉效果，
 *       但亮度值会被记忆，下次 LED_Enable 后自动应用
 * ----------------------------------------------------------------------- */
void LED_SetBrightness(uint8_t level);

/* -----------------------------------------------------------------------
 * LED_GetBrightness
 *   返回当前亮度设置值（0 ~ 100）。
 *   可用于显示当前亮度、按比例递增/递减等上层逻辑。
 * ----------------------------------------------------------------------- */
uint8_t LED_GetBrightness(void);

#endif /* _LED_H */
